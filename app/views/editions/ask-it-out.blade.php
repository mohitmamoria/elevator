		<h4 class="heading-edition">You Don't Get What You Don't Ask For</h4>
		<p>We, humans are afraid to ask things - including <em>'asking people to buy our product'</em>, <em>'asking that girl out'</em>, <em>'asking for a raise'</em>.</p>
		<blockquote>
			<p>We're afraid to ask because we fear rejection.</p>
		</blockquote>

		<p>Rejection is unbelievably powerful to derail people from asking for that thing again. It makes us feel we don't deserve what we are asking for. The feeling of getting rejected after you've asked someone out for a date sticks with you for years to come.</p>
		<blockquote>
			<p>100 people could tell you how freaking amazing you look today, but if one person says you look like crap, those 100 positive messages won't matter.</p>
		</blockquote>

