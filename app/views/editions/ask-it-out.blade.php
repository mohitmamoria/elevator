		<span class="wisdom-label"><a href="#how-to-ask-good-questions"><i class="fa fa-anchor"></i></a> Article</span>
		<h4 class="heading-edition">How to Be Amazingly Good at Asking Questions</h4>
		<p>When asking a question, you should be very clear about what you need for an answer. Do you need a factually correct answer? Do you need an expert opinion? Do you need a well-reasoned judgment? Be precise.</p>

		<ul>
			<li><strong>Don't ask Yes/No questions</strong>: Instead ask the questions with an open-end. Questions with 'who,' 'what,' 'where,' 'when,' 'how,' or 'why' lead to people giving some thought to their answers.</li>
			<li><strong>Dig deeper</strong>: Always consider using follow-up questions like, 'What makes you think that?'</li>
			<li><strong>Stay silent</strong>: People have more information and will bring it out in moments of silence to fill in the gaps.</li>
			<li><strong>Don't interupt</strong>: Ask your question, then let the person answer it in full, even when you think you are not getting the answer you want.</li>
		</ul>

