						<p>Winters - the perfect season to laze around! But do you know, there are two guys at some far-off place, sitting in a basement, wrapped in blankets and <em>doing</em> the thing that you're only <em>planning</em> to do tomorrow.</p>
						<p>I don't know who they are. It's very probable that they are in their early 20s but I am not sure how they look like. I don't even know what they are working on, but I know one thing for sure - they are hustling right now (and it's past midnight in their timezone).</p>
						<p>They might fail at whatever they are doing. But does it matter? Once fallen, they will get up, dust their shirts off and start afresh. And someday you will find them all over the internet as yet another <strong>overnight success story</strong>.</p>
