					<article class="summary text-edition spaced-out-h padder-v">
						<h4 class="heading-edition">Why I Walked Away From a $12M Acquisition Offer 18 Months After Our Startupâ€™s Launch</h4>
						<p>This was the hardest decision Alex has ever made. The story is not how he got the offer or how he decline it, but why he declined it. Execs at a big software company approached Alex when they were doing monthly revenue of $70k and offered $12mn (14x annual revenue) to acquire. He owned more than 50% of the company and the offer was tempting but he declined. Here's why.</p>

						<blockquote><p>First-time founders care most about their exit. Every time after that, you focus on legacy.</p></blockquote>

						<ul>
							<li>After acquisition, simple products turn bloated</li>
							<li>The email promising "we will remain the same" always turns out to be bullshit</li>
							<li>He didn't set out to create a company for acquisition</li>
						</ul>

						<p>Finally, going by the growth, they would be worth $24mn in one year and $43mn in two years. There's no reason to get acquired then.</p>

