		<span class="wisdom-label">Blog Post</span>
		<h4 class="heading-edition">Product Strategy means saying "No"</h4>
		<p>Building a great product doesn't mean hundreds of useful feature. It is about delivering a cohesive feature-set within well defined parameters.</p>

		<blockquote><p>If you’re building a product, you have to be great at saying no. Not “maybe” or “later”. The only word is <strong>no</strong>.</p></blockquote>

		<p>There are always a lot reasons to say Yes.</p>

		<ul>
			<li><strong>Data says so</strong>: Add Tetris to your product and you’ll probably see a boost in engagement, but does that mean your product is better? Engagement is not everything.</li>
			<li><strong>It'll take a few minutes</strong>: A feature is like an iceberg. Estimated cost is just the tip of an iceberg. Actual cost is under the waters.</li>
			<li><strong>Customer will leave otherwise</strong>: How to make terrible software: 1. Make promises for one-off features for a single client. 2. Implement them. 3. Repeat.</li>
			<li><strong>We can make it optional</strong>: It removes complexity from the outer layer, but adds to it everywhere else. Less configuration the better!</li>
			<li><strong>Competitors already have it</strong>: That doesn’t mean it’s a good idea. It could be something they’re trying out. It could be a shit idea. It could be something they’re planning on killing.</li>
			<li><strong>This could be one</strong>: You can speculate that any unbuilt feature could transform your product. But speculation is all it is, nothing more.</li>
		</ul>

		<p>But "no" is important because no one keeps crap ideas on their roadmap. Saying no to shit features is easy. Real product decisions mean saying, "This is a really great idea, I can see why our customers would like it. Well done. <strong>But we’re not going to build it.</strong> Instead, here’s what we’re doing."</p>

