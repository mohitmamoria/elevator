		<h3 class="heading-edition">Here I Become Douchebag</h3>
		<p>I started doubting our product. I started doubting my decisions (of course, some were wrong but some were right too). I started doubting our existence as a startup. The ideas to shut it down started growing on me. I started calculating how much I had spent on this in the last couple of years and how much more runway was left. My co-founder had left (still friends though). Year-end maintenance cost of US entity burnt a huge hole in my pocket. I started wondering if I should recommend my whole team to some good place and think about my future from scratch again. (I now know how Elon Musk had been feeling when Tesla wasn't picking up.)</p>
		<p>I am telling you, I was being a dick. And I wasn't even doing a good job at it. I was kinda being a weird kind of dick. Probably because I started feeling the pressure from all around. I was doing a really bad job at handling pressure.</p>
