		<ul>
			<li>We had too many features and this was confusing them (and also increased the overall cost of product).</li>
			<li>The integration was too tight for their taste, which required observable amount of efforts and time.</li>
		</ul>

		<p>Not just users, I talked a lot to my team too to discuss things from their perspectives. I spent nights (they were sleepless anyway) wondering (or shedding tears) and I think, we've reached to some conclusion.</p>

		<p><strong>We will be shutting down all of our features except just one. It will bring down the running cost, thus costing less to our users while also making the integration of no longer than 10 minutes.</strong></p>

