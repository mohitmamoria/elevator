		<h3 class="heading-edition">Here I Return</h3>
		<p>But hey, that was whole 2 days ago. Now, I am back. That too, with a BANG!</p>
		<p><em>Did I get cash in bank from somewhere? Did I get some idea to fix the things? What made me return back?</em> This...</p>
